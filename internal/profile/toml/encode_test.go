package toml_test

import (
	"testing"

	"ciphera/tools/internal/fixtures"
	"ciphera/tools/internal/policy"
	"ciphera/tools/internal/profile/toml"
	"ciphera/tools/internal/requirementid"
	"ciphera/tools/internal/style"
)

func TestEncodeRoundTripsLocalProfile(t *testing.T) {
	t.Parallel()

	config := policy.Config{
		SchemaVersion: policy.SchemaVersion,
		Repository: policy.RepositoryConfig{
			RootMarkers: []string{"PROJECT.marker"},
			ScopeRoots: map[style.Scope][]string{
				style.Scope("app"): {"."},
			},
			DefaultScope:        style.Scope("app"),
			ExcludedDirectories: []string{"vendor/"},
			GeneratedMarker:     "Code generated by",
		},
		StyleGuide: policy.StyleGuideConfig{
			Path:     "STYLE.md",
			IDScheme: requirementid.SectionSlug,
		},
		PathRoles: policy.PathRoles{
			"go_source": {"internal/"},
		},
		FileSets: policy.FileSets{
			{
				Name: "go",
				Include: policy.FileSetInclude{
					Extensions: []string{".go"},
					Paths: map[style.Scope][]string{
						style.Scope("app"): {"internal/"},
					},
				},
			},
		},
		Tools: policy.PinnedTools{
			{ID: "go", Version: "1.0.0"},
		},
		EnabledPacks: []string{"go"},
		Rules: []policy.RuleBinding{
			{
				RuleID:         "go/test",
				Enforcement:    style.EnforcementRequired,
				Scope:          style.Scope("app"),
				RequirementIDs: []string{"1.1.test-rule"},
			},
		},
	}

	contents, err := toml.Encode(config)
	if err != nil {
		t.Fatalf("Encode: %v", err)
	}

	got, err := toml.Decode(contents)
	if err != nil {
		t.Fatalf("Decode encoded profile: %v", err)
	}

	requireConfigEqual(t, config, got)
}

